# დავალება 4: 10 სიტყვის შეყვანა და მხოლოდ სწორის დაბეჭდვა

x = "აეიოუ"

for i in range(10):
    user_input = input("გთხოვთ შეიყვანოთ რაიმე სიტყვა რომელიც იწყება და მთავრდება თანხმოვანზე: ")
    if user_input[0] in x or user_input[-1] in x:
        print("თქვენს მიერ შეყვანილი სიტყვა არასწორია!")
    else:
        print(user_input)
        print("გმადლობთ, თქვენ შეიყვანეთ სწორი სიტყვა!")


