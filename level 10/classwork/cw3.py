number1 = int(input("Please enter a number: "))
number2 = int(input("Please enter a number 2: "))

if number2 > number1:
    print("მეორე რიცხვი პირველზე მეტია.")

if number2 < number1:
    print("მეორე რიცხი პირველზე ნაკლებია.")